    private void btnKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajActionPerformed
        if (tfKorisnickoIme.getText().trim().isEmpty() || tfZaporka.getText().trim().isEmpty() || tfIme.getText().trim().isEmpty() || tfPrezime.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Potrebno je ispuniti sva polja!");
        }  else {

            try {
                Repository sqlRepository = RepositoryFactory.getRepository();
                String korisnickoIme = tfKorisnickoIme.getText();
                String zaporka = tfZaporka.getText();
                String ime = tfIme.getText();
                String prezime = tfPrezime.getText();
                int TipKorisnikaID = 2;

                int korisnikBroj = sqlRepository.registracijaKorisnik(korisnickoIme, zaporka, TipKorisnikaID, ime, prezime, this);
                if (korisnikBroj != 0) {
                    JOptionPane.showMessageDialog(this, "Korisnik broj " + korisnikBroj + " uspjesno kreiran!");
                } else {
                    JOptionPane.showMessageDialog(this, "Greska kod kreiranja korisnika!");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Upi≈°ite pravilne informacije");
            }
        }
    }//GEN-LAST:event_btnKreirajActionPerformed

